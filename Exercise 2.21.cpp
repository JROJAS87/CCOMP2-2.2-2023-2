// (Displaying Large Letters)
// Write a program that prints C++ as follows:
//    CCC   +      +
//   C      +      +
//  C     +++++  +++++
//   C      +      +
//    CCC   +      +


#include <iostream>

int main() {
    std::cout << "   CCC   +      +" << std::endl;
    std::cout << "  C      +      +" << std::endl;
    std::cout << " C     +++++  +++++" << std::endl;
    std::cout << "  C      +      +" << std::endl;
    std::cout << "   CCC   +      +" << std::endl;
    return 0;
}
